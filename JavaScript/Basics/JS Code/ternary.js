//ternary operators

const x = 9 ;
const color = x>10 ? 'red' : 'blue';
console.log(color);


// switch

switch(color) {
    case 'red':
        console.log('Color is red');
        break;
    case 'blue':
        console.log('Color is Blue');
        break;
    default:
        console.log('Color is not red or blue');
}
